        features = params.get("features", [])
        installation = params.get("installation", "")
        usage = params.get("usage", "")
        badges = params.get("badges", [])
        language = params.get("language", "generic")
        advanced = params.get("advanced", False)
        ai_enhance = params.get("ai_enhance", False)

        base = (
            self._advanced_template(name, description, features, installation, usage, badges, language)
            if advanced else
            self._basic_template(name, description, features, installation, usage, language)
        )

        if ai_enhance and self.chat:
            prompt = self._build_ai_prompt(base, name)
            enhanced = await self.chat.ask_full(prompt)
            return enhanced.strip()

        return base

